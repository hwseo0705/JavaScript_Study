// boolean type
var logic = 10 < 5;
console.log('Logic Type: ' + typeof logic);
console.log('Logic: ' + logic);

var x = true;
console.log('X Type: ' + typeof x);
console.log('X: ' + x);

// undefined type/value
// undefined when you declare but not assign value (initialize)
// '' is not undefined
var fruit;
console.log('Fruit: ' + fruit);

// null type --> value = null
// a developer intentionally set the value to null
// 0 is not null
var apple = 'APPLE';
apple = null;
var money = '50000';
money = null;