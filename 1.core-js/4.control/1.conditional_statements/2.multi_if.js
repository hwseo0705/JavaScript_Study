var age = 25;
if (age >= 20) {
    console.log("Adult");
} else if (age >= 17) {
    console.log("Minor - High School");
} else if (age >= 14) {
    console.log("Minor - Middle School");
} else if (age >= 8) {
    console.log("Minor - Elementary School");
} else {
    console.log("You are too young!");
}